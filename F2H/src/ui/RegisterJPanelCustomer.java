    private void addUserToOrgAndEnterprise(Class userEnterpriseClass, Class userOrganizationClass, UserAccount user) {
        ArrayList<Network> networkList = business.getNetworkList();
        for (Network network : networkList) {
            ArrayList<Enterprise> enterpriseList = network.getEnterpriseDirectory().getEnterpriseList();
            for (Enterprise enterprise : enterpriseList) {
                if (enterprise.getClass().equals(userEnterpriseClass)) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganisationList()) {
                        if (organization.getClass().equals(userOrganizationClass)) {
                            organization.getUserAccountDirectory().getUserAccountList().add(user);
                            break;
                        }
                    }
                }
            }
        }
    }

