    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
         
        String password = txtPassword.getText();
        String firstName = txtFirstname.getText();
        String lastName = txtLastName.getText();
        String email = txtEmail.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();

        String role = (String) comboBoxRole.getSelectedItem();

        if (username.isEmpty() || password.isEmpty() || firstName.isEmpty() || lastName.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty() || role == null) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid email address.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!isValidPhoneNumber(phone)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid phone number.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (business.getUserAccountDirectory().checkIfUsernameIsUnique(username)) {
            Person person = new Person(firstName, lastName, email, phone, address);
            business.getPersonDirectory().addNewPerson(person);
            UserAccount account = null;

            if (role.equalsIgnoreCase("Volunteer")) {
                VounteerEmp volunteer = new VounteerEmp(); // Assuming this is a class
                account = business.getUserAccountDirectory().createUserAccount(person, username, password, volunteer, new Volunteer());
                addUserToOrgAndEnterprise(KnowledgeTransferEnterprise.class, VolunteersOrganization.class, account);
            } else if (role.equalsIgnoreCase("Researcher")) {
                ResearcherEmp researcher = new ResearcherEmp();
                account = business.getUserAccountDirectory().createUserAccount(person, username, password, researcher, new Researcher());
                addUserToOrgAndEnterprise(KnowledgeTransferEnterprise.class, ResearchersOrganization.class, account);
            } else if (role.equalsIgnoreCase("Customer")) {
                account = business.getUserAccountDirectory().createUserAccount(person, username, password, null, new CustomerRole());
                addUserToOrgAndEnterprise(SalesAndDeliveryEnterprise.class, OnlineStore.class, account);
            } else if (role.equalsIgnoreCase("Farmer")) {

                account = business.getUserAccountDirectory().createUserAccount(person, username, password, null, new FarmerRole());
                addUserToOrgAndEnterprise(ProductionEnterprise.class, FarmersOrganization.class, account);
            } else if (role.equalsIgnoreCase("Poultry Producer")) {

                account = business.getUserAccountDirectory().createUserAccount(person, username, password, null, new FarmerRole());
                addUserToOrgAndEnterprise(ProductionEnterprise.class, FarmersOrganization.class, account);
            } else if (role.equalsIgnoreCase("Honey & Other Commodities Cultivator")) {

                account = business.getUserAccountDirectory().createUserAccount(person, username, password, null, new FarmerRole());
                addUserToOrgAndEnterprise(ProductionEnterprise.class, FarmersOrganization.class, account);
            } else if (role.equalsIgnoreCase("Certification Specialist")) {
                CertificationSpecialistEmp certificationSpecialist = new CertificationSpecialistEmp();
                account = business.getUserAccountDirectory().createUserAccount(person, username, password, certificationSpecialist, new CertificationSpecialist());
                addUserToOrgAndEnterprise(QualityAssuranceEnterprise.class, AccreditationBoardOrganization.class, account);
            } else if (role.equalsIgnoreCase("Delivery Person")) {
                DeliveryEmp deliveryPerson = new DeliveryEmp();
                account = business.getUserAccountDirectory().createUserAccount(person, username, password, deliveryPerson, new DeliveryPerson());
                addUserToOrgAndEnterprise(SalesAndDeliveryEnterprise.class, DeliveryOrganization.class, account);
            }

            JOptionPane.showMessageDialog(null, "User registered successfully.", "Information", JOptionPane.INFORMATION_MESSAGE);
            clearAll();
        } else {
            JOptionPane.showMessageDialog(null, "Username already exists.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

