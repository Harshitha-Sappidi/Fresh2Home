    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblordersummary.getSelectedRow();
    if (selectedRow >= 0) {
        int orderId = (int) tblordersummary.getValueAt(selectedRow, 0);
        String farmerIds = (String) tblordersummary.getValueAt(selectedRow, 5); // Retrieve farmer IDs
        int rating = prioritySlider.getValue();
        String comments = jTextArea1.getText();

        // Print current values for debugging
        System.out.println("Selected Order ID: " + orderId);
        System.out.println("Farmer IDs: " + farmerIds);
        System.out.println("Selected Rating: " + rating);
        System.out.println("Comments: " + comments);

        // Handling the first farmer ID for simplicity
        String farmerId = farmerIds.split(",")[0].trim();

        // Print the farmer ID being used for the review
        System.out.println("Farmer ID for Review: " + farmerId);

        // Create a new review object
        Review newReview = new Review(orderId, farmerId, rating, comments);
        business.getReviewDirectory().getReviewList().add(newReview);
        

        // Confirmation message
        JOptionPane.showMessageDialog(null, "Review added successfully!");
        System.out.println("Review successfully added to the system.");

        // Resetting the text area and hiding the feedback panel
        jTextArea1.setText("");
        jPanel1.setVisible(false); // Hide the feedback panel after adding the review

    } else {
        JOptionPane.showMessageDialog(null, "Please select an order to add a review.");
    }
    }//GEN-LAST:event_btnAddActionPerformed


