    public void populateTable() {
    DefaultTableModel model = (DefaultTableModel) tblordersummary.getModel();
    model.setRowCount(0);

    for (Order order : Order.ordersList) {
        if (order.getCustomerId().equalsIgnoreCase(account.getUsername())) {
            Object[] row = new Object[6]; // Include columns for Order ID, Date, Total Amount, Status, Products, Farmer IDs
            row[0] = order.getOrderId();
            row[1] = order.getOrderDate();
            row[2] = order.getTotalAmount();
            row[3] = order.getOrderStatus();
            StringBuilder productNames = new StringBuilder();
            StringBuilder farmerIds = new StringBuilder();

            // Iterate over each product in the cart
            for (Map.Entry<Integer, Product> entry : order.getCart().getProductMap().entrySet()) {
                Product product = business.getProductDirectory().getProductFromProductID(entry.getKey());
                if (product != null) {
                    if (productNames.length() > 0) {
                        productNames.append(", ");
                        farmerIds.append(", "); // Adding separator for readability
                    }
                    productNames.append(product.getProductname());
                    farmerIds.append(product.getFarmersId()); // Append farmer ID associated with each product
                }
            }

            row[4] = productNames.toString();
            row[5] = farmerIds.toString(); // Set farmer IDs
            model.addRow(row);
        }
    }
}


    
    

